#include<stdio.h>

int main()
{
    //code for bitwise operator
    int a,b;
    a=20;
    b=10;
    printf(" a&b %d\n",a&b);
    printf("a|b %d\n",a|b);
    printf(" a^b %d\n",a^b);
    printf(" ~a %d\n",~a);
    printf("a>>b %d\n",a>>b);
    printf(" a<<b %d\n",a<<b);
    return 0;
    //program for relational operators
}
