#include<stdio.h>

int main()
{
    int a,b;
    a=20;
    b=10;
    printf("%d\n a>b",a>b);
    printf("%d\n a<b",a<b);
    printf("%d\n a>=b",a>=b);
    printf("%d\n a<=b",a<=b);
    printf("%d\n a==b",a==b);
    printf("%d\n a!=b",a!=b);
    return 0;
    //program for relational operators
}
